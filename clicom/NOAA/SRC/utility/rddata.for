      SUBROUTINE OPENINPUT(RECTYPE,DATASOURCE)
C
C   SUBROUTINE TO OPEN INPUT FILE 25 USING THE APPROPRIATE FILE NAME.         
C       HOURLY DATA IS IN DATAEASE FORM "HOURLY DATA" OR "H HLY DATA"
C       DAILY DATA IS IN DATAEASE FORM "DAILY DATA" OR "H DLY DATA"
C       SYNOPTIC DATA IS IN DATAEASE FORM "SYNOPTIC DATA" OR "H SYN DATA"
C       UPPER AIR DATA IS IN DATAEASE FORM "UPPER AIR DATA" OR "H U-A
C       MONTHLY DATA IS IN DATAEASE FORM "MONTHLY DATA".
C
C  PROGRAM CONTROL VARIABLES
C
      CHARACTER*1 DATASOURCE
      CHARACTER*3 RECTYPE,TYPDEF(7) 
      CHARACTER*22 FILNAME
      CHARACTER*24 FRMNAM(14)
      INTEGER*4 NUMREC
      DATA TYPDEF /'MLY','10D','DLY','SYN','HLY','15M','U-A'/
      DATA FRMNAM /'MONTHLY DATA','TEN DAY DATA','DAILY DATA'
     +   ,'SYNOPTIC DATA','HOURLY DATA','FIFTEEN MINUTE DATA'
     +   ,'UPPER-AIR DATA','H MLY DATA','H 10D DATA','H DLY DATA'
     +   ,'H SYN DATA','H HLY DATA','H 15M DATA','H U-A DATA'/
C
C   FIND THE RELATIVE NUMBER OF THE DATATYPE WANTED TO INDEX INTO THE
C   FRMNAM ARRAY
C
      DO 20 I = 1,7
         IF (RECTYPE.EQ.TYPDEF(I)) THEN
            ITYPE = I
            GO TO 30
         END IF
20    CONTINUE
      CALL WRTMSG(3,81,12,1,1,RECTYPE,3)
      STOP 2
30    CONTINUE
      IF (DATASOURCE.NE.'M'.AND.DATASOURCE.NE.'m'.AND.DATASOURCE.NE.
     +     'S'.AND.DATASOURCE.NE.'s') THEN
         CALL WRTMSG(3,83,12,1,1,DATASOURCE,1)
         DATASOURCE = 'S'
      END IF
      IF (DATASOURCE.EQ.'S'.OR.DATASOURCE.EQ.'s') THEN
         ITYPE = ITYPE + 7
      END IF
C  
C   FIND THE FILE NAME OF THE DATAEASE FORM WANTED
C
      CALL FNDFIL(FRMNAM(ITYPE),FILNAME,NUMREC)
      IF (FILNAME.EQ.'       ') THEN
         STOP 2
      END IF
C   
C   OPEN THE DATA FILE WANTED
C
200   CONTINUE
      OPEN (25,FILE=FILNAME,STATUS='OLD',FORM='BINARY',SHARE='DENYNONE'
     +       ,MODE='READ',IOSTAT=IOCHK)
      IF (IOCHK.NE.0) THEN
         CALL OPENMSG(FILNAME,'OPENINPUT   ',IOCHK)
         GO TO 200
      END IF
      RETURN
      END
