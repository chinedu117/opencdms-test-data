      SUBROUTINE READMLY(DDSID,STNID,ELEM,YEAR,VALUE,FLAG1,RTNCODE)     
C
C    THIS SUBROUTINE READS A MLY FORMAT DATAEASE RECORD
C       THE INPUT DATA IS READ AS AN UNFORMATTED BINARY FILE,
C       READING ALL INPUT INTO THE CHARACTER ARRAY INREC.  THE 
C       VARIABLES WITHIN THE RECORD ARE EQUIVALENCED TO THE 
C       APPROPRIATE POSITION WITHIN INREC.
C
C  LOCAL VARIABLES
      CHARACTER*8 LOCALID
      CHARACTER*4 IYEAR
      CHARACTER*3 IELEM,IDDSID
      CHARACTER*1 INREC(84) 
      CHARACTER*84 INREC2
      INTEGER*2 HEADER(5)
C  PASSED VARIABLES
      CHARACTER*8 STNID
      INTEGER*2 DDSID,ELEM,YEAR
      CHARACTER*4 VALUE(12)
      CHARACTER*1 FLAG1(12),RTNCODE
C
C    EQUIVALENCE THE INPUT VARIABLES TO THE INPUT RECORD STRING
C
      EQUIVALENCE (HEADER,INREC(1)),(IDDSID,INREC(7)), 
     +            (LOCALID,INREC(10)),
     +            (IELEM,INREC(18)),(IYEAR,INREC(21))
      EQUIVALENCE (INREC(1),INREC2)
C
      RTNCODE = '0'
   10 CONTINUE
      READ(25,END=100) INREC
      IF(HEADER(1).NE.12.AND.HEADER(1).NE.14) GO TO 10
      STNID = LOCALID
      READ(IDDSID,'(I3)') DDSID
      READ(IYEAR,'(I4)') YEAR
      READ(IELEM,'(I3)') ELEM
      DO 50 I=1,12
         IPOS = 25 + (I-1)*5
         VALUE(I) = INREC2(IPOS:IPOS+3)
         FLAG1(I) = INREC(IPOS+4)
   50 CONTINUE
      RETURN   
C      
  100 CONTINUE
      RTNCODE = '1'
      RETURN
      END
