      SUBROUTINE READNML (STNID,PERIOD,MONTH,ELEM,NORMAL,NMLTYP
     +                   ,NORMFLG,SDEV,EXTMAX,EMAXFLG,MAXYR
     +                   ,EXTMIN,EMINFLG,MINYR,MLYMAXMN,SDMAX
     +                   ,MLYMINMN,SDMIN,PQM,PQX,PQN,BITVAL,RTNCODE)
C
C    THIS SUBROUTINE READS A NORMALS FORMAT DATAEASE RECORD
C       THE INPUT DATA IS READ AS AN UNFORMATTED BINARY FILE,
C       READING ALL INPUT INTO THE CHARACTER ARRAY INREC.  THE 
C       VARIABLES WITHIN THE RECORD ARE EQUIVALENCED TO THE 
C       APPROPRIATE POSITION WITHIN INREC.
C
C   NOTE:  NUMERIC VARIABLES ARE EQUIVALENCED TO THE CHARACTER VARIABLE INREC.
C          SOME OF THE NUMERIC VARIABLES START ON ODD-BYTE BOUNDARIES.  EVEN
C          THOUGH THE DOCUMENTATION FOR MICROSOFT 5.0 FORTRAN SPECIFIES THAT
C          NON-CHARACTER ENTITIES MUST START ON EVEN-BYTE BOUNDARIES, THIS IS
C          NOT AN ACTUAL FORTRAN REQUIREMENT.  THIS HAS BEEN VERIFIED WITH
C          MICROSOFT TECHNICAL SUPPORT.  THE CURRENT EQUIVALENCES WORK AS
C          WRITTEN.  (5-20-91)
C
C       .. PASSED VARIABLES
      CHARACTER*8 STNID,PERIOD
      CHARACTER*4 MAXYR,MINYR
      CHARACTER*3 ELEM
      CHARACTER*1 RTNCODE,EMAXFLG,EMINFLG
      INTEGER*2 MONTH,NMLTYP,NORMFLG,BITVAL(30)
      REAL*4 NORMAL,SDEV,EXTMAX,EXTMIN,MLYMAXMN,MLYMINMN,SDMAX,
     +       SDMIN,PQM(5),PQX(5),PQN(5)
C     
C       .. LOCAL VARIABLES
C
      CHARACTER*1 INREC(165)
C
      CHARACTER*8 LOCALID,NPERIOD
      CHARACTER*4 MXYR,MNYR
      CHARACTER*3 ELEM1
      CHARACTER*2 OMONTH
      CHARACTER*1 EMAXFG,EMINFG
      INTEGER*1 TYPNML,NMLFG
      INTEGER*2 HEADER(2)
      REAL*4 NORM,MEAN,EMAX,EMIN,MYMX,MYMN,SDEMAX,SDEMIN
     +      ,P1(5),P2(5),P3(5) 
C
C
C    EQUIVALENCE THE INPUT VARIABLES TO THE INPUT RECORD STRING
C
      EQUIVALENCE (HEADER,INREC(1)),(LOCALID,INREC(11)),
     +            (NPERIOD,INREC(19)),(OMONTH,INREC(27)),
     +            (ELEM1,INREC(29)),(NORM,INREC(32)),(TYPNML,INREC(36)),
     +            (NMLFG,INREC(37)),(MEAN,INREC(38)),(EMAX,INREC(42)),
     +            (EMAXFG,INREC(46)),(MXYR,INREC(47)),(EMIN,INREC(51)),
     +            (EMINFG,INREC(55)),(MNYR,INREC(56)),(P1,INREC(60)),
     +            (MYMX,INREC(80)),(SDEMAX,INREC(84)),(P2,INREC(88)),
     +            (MYMN,INREC(108)),(SDEMIN,INREC(112)),(P3,INREC(116))
C
      RTNCODE = '0'
   10 CONTINUE
      READ(25,END=100) INREC
      IF(HEADER(1).NE.12.AND.HEADER(1).NE.14) GO TO 10
      STNID = LOCALID
      PERIOD = NPERIOD
      READ(OMONTH,'(I2)') MONTH
      ELEM = ELEM1
      NORMAL = NORM
      NMLTYP = INT2(TYPNML)
      NORMFLG = INT2(NMLFG)
      SDEV = MEAN
      EXTMAX = EMAX
      EMAXFLG = EMAXFG
      MAXYR = MXYR
      EXTMIN = EMIN
      EMINFLG = EMINFG
      MINYR = MNYR
      MLYMAXMN = MYMX
      SDMAX = SDEMAX
      MLYMINMN = MYMN
      SDMIN = SDEMIN
C
      DO 50 I=1,5
          PQM(I) = P1(I)
          PQX(I) = P2(I)
          PQN(I) = P3(I)
  50  CONTINUE
C
      DO 75 I=1,30
         IPOS = 136+ (I-1)
         BITVAL(I) = INREC(IPOS)
   75 CONTINUE
      RETURN   
C
  100 CONTINUE
      RTNCODE = '1'
      RETURN
      END
